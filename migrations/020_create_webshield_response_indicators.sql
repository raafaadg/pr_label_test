CREATE TABLE eco_webshield_indicator
(
    process_id varchar(512),
    indicator int(20),
    label varchar(100),
    comment varchar(512),
    score varchar(100),

    created_at datetime DEFAULT CURRENT_TIMESTAMP not null,

    primary key (process_id, indicator, created_at),
    index (process_id),
    index (indicator),
    index (score)
);

